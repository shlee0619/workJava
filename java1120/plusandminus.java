package java1120;

public class plusandminus {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        a = 3;
        b = 7;
        System.out.println(a%b);
        System.out.println(b%a);

        a++;
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);
    }
}
